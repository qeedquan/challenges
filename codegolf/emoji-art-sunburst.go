/*

@Arnauld

Method
For each coordinates (x,y) ∈ [0…w-1]^2, we compute:

X = x - w/2 + 1/2
Y = y - w/2 + 1/2

and then convert (X,Y) to polar coordinates (r,ϕ):

r = sqrt(X^2 + Y^2)
ϕ = arctan(X / Y)

The test for the sun is:

r < w/8 ⟺ X^2 + Y^2 < w^2/64

The color of each ray is obtained by testing the parity of:

⌊ϕ × 16π + 1/2⌋≈⌊ϕ × 5.09 + 0.5⌋

*/

func sunburst(n int) {
	fmt.Printf("n=%d\n", n)
	for y := range n {
		for x := range n {
			w := float64(n)
			X := float64(x) - w*0.5 + 0.5
			Y := float64(y) - w*0.5 + 0.5
			r := math.Hypot(X, Y)
			ϕ := math.Atan(X / Y)
			z := math.Floor(ϕ*16/math.Pi + 0.5)
			switch {
			case r < w/8:
				fmt.Printf("🟨")
			case int(z)&1 == 0:
				fmt.Printf("🟧")
			default:
				fmt.Printf("🟥")
			}
		}
		fmt.Println()
	}
	fmt.Println()
}
