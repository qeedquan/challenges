func gen(l int) []int {
	r := []int{}
	for n := 0; n < l; n++ {
		i, j, b := 1, 1, make(map[int]bool)
		for n-2*i >= 0 {
			b[2*r[n-i]-r[n-2*i]] = true
			for i++; b[j] != false; j++ {
				delete(b, j)
			}
		}
		r = append(r, j)
	}
	return r
}
