#include <stdio.h>

int
interpret(const char *s)
{
	size_t i;
	int ac;

	ac = 0;
	for (i = 0; s[i]; i++) {
		switch (s[i]) {
		case '+':
			ac++;
			break;
		case '-':
			putchar(ac--);
			break;
		}
		ac &= 0xff;
	}
	puts("");

	return ac;
}

