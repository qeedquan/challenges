https://i.stack.imgur.com/IY4Nu.gif

Some explanations
We have 28 pixels around a square of 8 x 8. building a shape array, growing from 1 to 27, making a full loop with each length, then shrinking from 26 to 2 in order to loop correctly, we must obtain: ( 27 + 25 ) * 28 = 1456 shapes.

Here is how look 2 1st shapes:

[0]=$'\e[A\U2801\U2800\U2800\U2800\e[B\e[4D\U2800\U2800\U2800\U2800'
[1]=$'\e[A\U2808\U2800\U2800\U2800\e[B\e[4D\U2800\U2800\U2800\U2800'
As shape use two lines, I use ANSI escape sequences for moving cursor:

\e[A : Move cursor up
\e[B : Move cursor down
\e[D : Move cursor left (\e[4D move 4 position left)
In order to ensure correct placement even using non monospace font, whitespace are drawn by using BRAILLE PATTERN BLANK (\U2800)
My question:
What's the shortest way to populate $shapes array.

The goal of this is to build the array of 1456 shapes, for a 8x8 square... But even if user want a smaller square, I think, the ideal tool must accept as optional argument, an integer, between 2 to 8, specifying the size of desired square!

So in fine.

The tool could be written in any language,
If no argument are given, the tool must compute a progessive snake around a 8x8 square
The snake
Start at upper left corner with 1 pixel,
Do a full turn around desired size square starting horizontally,
Grown by 1 pixel at begin of each new turn, upto full size - 1 pixel, then
shrink by 1 pixed at begin of each new turn, upto 2 pixel.
If argument between 2 to 8 are given, the tool must compute a progessive snake around a (smaller) square of given size.
The output could be
A full bash array, as cited sample,
space separated
new line separated
This is a code-golf, so shorter code win.

