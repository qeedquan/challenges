package main

import "fmt"

func main() {
	output()
}

// ported from @r3mainer solution
func output() {
	const sym = "bSK8C?A;6HMI927B@Z4UQ"

	var (
		m [112][113]byte
		i int
	)
	for y := range m {
		for x := range m {
			if m[y][x] != 0 {
				continue
			}

			n := int(sym[i]) - '0'
			i += 1
			for p := 0; p < n; p++ {
				for q := 0; q < n; q++ {
					c := byte('#')
					if p != 0 && n-1-p != 0 && q != 0 && n-1-q != 0 {
						c = ' '
					}
					m[y+q][x+p] = c
				}
			}
		}
		fmt.Printf("%s\n", m[y][:len(m)])
	}
}
