#include <math.h>

#define nelem(x) (sizeof(x) / sizeof(x[0]))

int
psq(int x)
{
	int s;

	s = sqrt(x);
	return (s * s) == x;
}

// http://oeis.org/A242445
int
st(int x)
{
	int y, z;

	for (y = 1; y < x; y++) {
		for (z = y + 1; z < x; z++) {
			if (psq(x + y) && psq(x + z) && psq(y + z))
				return 1;
		}
	}
	return 0;
}

