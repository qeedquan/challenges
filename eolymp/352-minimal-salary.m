%{

In a small company, there are at least two employees—the director and the chief accountant.
Given the salaries of all employees, find the minimal salary in the company.

Input
The salaries of the employees are given in one line, in the national currency, separated by spaces.
All input values are non-negative real numbers, and the number of salaries does not exceed 1000.

Output
Print the minimal salary in the company with two digits after the decimal point.

Examples

Input #1
1000.00 760.00

Answer #1
760.00

%}

min([1000, 760])

