{-

The program takes two natural numbers, a and n, as input, each provided on a separate line. The output should be the result of a^n.

Input
Two natural numbers, a and n, each on its own line.

Output
The result of a^n.

Examples

Input #1
2
10

Answer #1
1024

-}

solve :: Int -> Int -> Int
solve a n = a ^ n

