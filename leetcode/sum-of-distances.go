/*

You are given a 0-indexed integer array nums.
There exists an array arr of length nums.length, where arr[i] is the sum of |i - j| over all j such that nums[j] == nums[i] and j != i.
If there is no such j, set arr[i] to be 0.

Return the array arr.

Example 1:

Input: nums = [1,3,1,1,2]
Output: [5,0,3,4,0]
Explanation:
When i = 0, nums[0] == nums[2] and nums[0] == nums[3]. Therefore, arr[0] = |0 - 2| + |0 - 3| = 5.
When i = 1, arr[1] = 0 because there is no other index with value 3.
When i = 2, nums[2] == nums[0] and nums[2] == nums[3]. Therefore, arr[2] = |2 - 0| + |2 - 3| = 3.
When i = 3, nums[3] == nums[0] and nums[3] == nums[2]. Therefore, arr[3] = |3 - 0| + |3 - 2| = 4.
When i = 4, arr[4] = 0 because there is no other index with value 2.

Example 2:

Input: nums = [0,5,3]
Output: [0,0,0]
Explanation: Since each element in nums is distinct, arr[i] = 0 for all i.


Constraints:

1 <= nums.length <= 10^5
0 <= nums[i] <= 10^9

Note: This question is the same as 2121: Intervals Between Identical Elements.

Hint 1
Can we use the prefix sum here?

Hint 2
For each number x, collect all the indices where x occurs, and calculate the prefix sum of the array.

Hint 3
For each occurrence of x, the indices to the right will be regular subtraction while the indices to the left will be reversed subtraction.

*/

package main

import (
	"fmt"
	"reflect"
)

func main() {
	test(
		[]int{1, 3, 1, 1, 2},
		[]int{5, 0, 3, 4, 0},
	)

	test(
		[]int{0, 5, 3},
		[]int{0, 0, 0},
	)
}

func assert(x bool) {
	if !x {
		panic("assertion failed")
	}
}

func test(a, r []int) {
	p := distance(a)
	fmt.Println(p)
	assert(reflect.DeepEqual(p, r))
}

func distance(a []int) []int {
	n := len(a)
	r := make([]int, n)

	c1 := make(map[int]int)
	d1 := make(map[int]int)
	for i := range n {
		x := a[i]
		r[i] += c1[x]*i - d1[x]
		c1[x] += 1
		d1[x] += i
	}

	c2 := make(map[int]int)
	d2 := make(map[int]int)
	for i := n - 1; i >= 0; i-- {
		x := a[i]
		r[i] += d2[x] - c2[x]*i
		c2[x] += 1
		d2[x] += i
	}

	return r
}
