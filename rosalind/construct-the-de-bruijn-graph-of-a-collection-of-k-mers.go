func debruijn(patterns []string) map[string][]string {
	table := make(map[string][]string)
	for _, pattern := range patterns {
		length := len(pattern)
		if length == 0 {
			continue
		}

		key := pattern[:length-1]
		table[key] = append(table[key], pattern[1:])
	}
	return table
}
