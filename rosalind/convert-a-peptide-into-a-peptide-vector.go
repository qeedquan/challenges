func test(peptide string, expected []int) {
	vector := peptide2vector(peptide)
	fmt.Println(vector)
	assert(reflect.DeepEqual(vector, expected))
}

func peptide2vector(peptide string) []int {
	ams := map[rune]int{
		'A': 71, 'C': 103, 'E': 129, 'D': 115, 'G': 57, 'F': 147, 'I': 113, 'H': 137, 'K': 128, 'M': 131, 'L': 113,
		'N': 114, 'Q': 128, 'P': 97, 'S': 87, 'R': 156, 'T': 101, 'W': 186, 'V': 99, 'Y': 163, 'X': 4, 'Z': 5,
	}

	indices := []int{}
	total := 0
	for _, symbol := range peptide {
		total += ams[symbol]
		indices = append(indices, total)
	}

	if len(indices) == 0 {
		return []int{}
	}

	vector := make([]int, indices[len(indices)-1])
	for _, index := range indices {
		vector[index-1] = 1
	}

	return vector
}
