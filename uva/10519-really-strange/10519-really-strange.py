#!/usr/bin/env python3

# https://oeis.org/A386480
def solve(n):
    if n < 0:
        return 0
    if n == 0:
        return 1
    if n == 1:
        return 2
    return n**2 - n + 2

def main():
    tab = [
        1, 2, 4, 8, 14, 22, 32, 44, 58, 74, 92, 112, 134, 158, 184, 212, 242,
        274, 308, 344, 382, 422, 464, 508, 554, 602, 652, 704, 758, 814, 872,
        932, 994, 1058, 1124, 1192, 1262, 1334, 1408, 1484, 1562, 1642, 1724,
        1808, 1894, 1982, 2072, 2164, 2258, 2354, 2452, 2552, 2654, 2758, 2864,
        2972, 3082, 3194, 3308, 3424, 3542, 3662, 3784
    ]

    assert(solve(3) == 8)
    assert(solve(4) == 14)

    for i in range(len(tab)):
        assert(solve(i) == tab[i])

main()

