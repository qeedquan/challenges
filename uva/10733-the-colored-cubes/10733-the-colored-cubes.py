#!/usr/bin/env python3

# https://oeis.org/A047780
def solve(n):
    return n**2*(n**4 + 3*n**2 + 12*n + 8)//24

def main():
    tab = [
        0, 1, 10, 57, 240, 800, 2226, 5390, 11712, 23355, 43450, 76351, 127920,
        205842, 319970, 482700, 709376, 1018725, 1433322, 1980085, 2690800,
        3602676, 4758930, 6209402, 8011200, 10229375, 12937626, 16219035,
        20166832, 24885190, 30490050
    ]

    assert(solve(1) == 1)
    assert(solve(2) == 10)

    for i in range(len(tab)):
        assert(solve(i) == tab[i])

main()

