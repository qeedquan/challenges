#!/usr/bin/env python3

"""

https://oeis.org/A001787

C(n,1) * (C(n-1,0) + C(n-1,1) + ... + C(n-1,n-1)) = n*(2^(n-1))

"""
def solve(n):
    if n < 1:
        return 0
    return (n * (1<<(n-1))) % 1000000007

def main():
    tab = [
        0, 1, 4, 12, 32, 80, 192, 448, 1024, 2304, 5120, 11264, 24576, 53248,
        114688, 245760, 524288, 1114112, 2359296, 4980736, 10485760, 22020096,
        46137344, 96468992, 201326592, 419430400, 872415232, 1811939328, 3758096384,
        7784628224, 16106127360, 33285996544
    ]

    assert(solve(1) == 1)
    assert(solve(2) == 4)
    assert(solve(3) == 12)

    for i in range(len(tab)):
        assert(solve(i) == tab[i] % 1000000007)

main()

